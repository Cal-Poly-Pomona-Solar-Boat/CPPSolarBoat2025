static uint16_t ADC_Convert(ADC_HandleTypeDef *hadc, uint32_t channel, uint32_t sample_time)
{
    ADC_ChannelConfTypeDef sConfig = {0};
    sConfig.Channel = channel;
    sConfig.Rank = ADC_REGULAR_RANK_1;
    sConfig.SamplingTime = sample_time;
    sConfig.SingleDiff = ADC_SINGLE_ENDED;
    sConfig.OffsetNumber = ADC_OFFSET_NONE;
    sConfig.Offset = 0;

    if (HAL_ADC_ConfigChannel(hadc, &sConfig) != HAL_OK) Error_Handler();

    HAL_ADC_Start(hadc);
    HAL_ADC_PollForConversion(hadc, 100);
    uint16_t v = (uint16_t)HAL_ADC_GetValue(hadc);
    HAL_ADC_Stop(hadc);
    return v;
}

static uint16_t constrain(uint16_t in, uint16_t min, uint16_t max)
{
	if(in > min && in < max) return in;
	if(in <= min) return min;
	return max;
}

static uint16_t map(uint16_t in, uint16_t inMin, uint16_t inMax, uint16_t outMin, uint16_t outMax)
{
	return (outMax - outMin) / (inMax - inMin) * (in - inMin);
}


